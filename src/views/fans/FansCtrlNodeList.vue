<script lang="ts" name="interact-fansCtrlNode" setup>
  import { ref, reactive } from 'vue';
  import { BasicTable, useTable, TableAction } from '/@/components/Table';
  import { useListPage } from '/@/hooks/system/useListPage';
  import { columns, superQuerySchema } from './FansCtrlNode.data';
  import { list, deleteOne, batchDelete, getImportUrl, getExportUrl } from './FansCtrlNode.api';
  import { downloadFile } from '/@/utils/common/renderUtils';
  import FansCtrlNodeModal from './components/FansCtrlNodeModal.vue'
  import { useUserStore } from '/@/store/modules/user';

  const formRef = ref();
  const queryParam = reactive<any>({});
  const toggleSearchStatus = ref<boolean>(false);
  const registerModal = ref();
  const userStore = useUserStore();
  //注册table数据
  const { prefixCls, tableContext, onExportXls, onImportXls } = useListPage({
    tableProps: {
      title: '云控设备',
      api: list,
      columns,
      canResize:false,
      useSearchForm: false,
      actionColumn: {
        width: 60,
        fixed: 'right',
      },
      beforeFetch: (params) => {
        return Object.assign(params, queryParam);
      },
    },
    exportConfig: {
      name: "云控设备",
      url: getExportUrl,
      params: queryParam,
    },
	  importConfig: {
